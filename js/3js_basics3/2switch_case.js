const month = "march"

switch (month) {
    case "jan":
        console.log("January");
        break;
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;

    default:                                    //note that if no break statement are used than:-when 1 condn come true than all remaining condn execute except default
        console.log("default case match");
        break;
}